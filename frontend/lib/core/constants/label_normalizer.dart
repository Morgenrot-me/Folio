  /// 对标签列表进行归一化处理：
  ///   1. 黑名单过滤
  ///   2. 同义词合并（同组只保留代表词）
  ///   3. 去重（保持输入顺序）
  ///   4. 返回最多 [maxCount] 个标签
  static List<String> normalize(List<String> tags, {int maxCount = 8}) {
    if (tags.isEmpty) return [];

    // Step 1: 黑名单过滤
    final filtered = tags.where((t) => !_blocklist.contains(t)).toList();

    // Step 2: 构建快速查找表：标签 → 该同义词组代表词
    final Map<String, String> toCanonical = {};
    for (final group in _synonymGroups) {
      final canonical = group.first;
      for (final synonym in group) {
        toCanonical[synonym] = canonical;
      }
    }

    // Step 3: 同义词合并 + 去重
    final seen = <String>{};
    final result = <String>[];
    for (final tag in filtered) {
      final canonical = toCanonical[tag] ?? tag;
      if (seen.add(canonical)) {
        result.add(canonical);
        if (result.length >= maxCount) break;
      }
    }

    return result;
  }
